public class relationaloperators 
{
    public static void main(String[] args) 
    {
        int a=2;
        int b=5;

        boolean result1= a < b;  //greater than
        System.out.println(result1);
    
        
        boolean result2= a > b;  //less than
        System.out.println(result2);
   

        boolean result3= a >= b;  //greater than or equal to
        System.out.println(result3);
    

        boolean result4= a <= b;  //less than or equal to
        System.out.println(result4);  
   

        boolean result5= a == b;  //equal to
        System.out.println(result5);
   

        boolean result6= a != b;  // not equal to
        System.out.println(result6);
    }
}
    
    

